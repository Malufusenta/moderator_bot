"""
Нормализация текста: два разных варианта под две разные задачи.

  normalize(text)    → канонический текст ДЛЯ АНТИ-ДУБЛЯ (раздел 6.4).
                       Оставляет только буквы/цифры/пробелы, схлопывает
                       whitespace. Именно это значение хешируется и идёт
                       в difflib.SequenceMatcher.

  deobfuscate(text)  → текст ДЛЯ ПОИСКА СТОП-СЛОВ (раздел 6.2).
                       Заменяет гомоглифы (лат. → кир., цифры → буквы),
                       удаляет zero-width символы, нормализует пробелы.
                       Пунктуацию НЕ удаляет — нужна для работы \b-границ
                       и для корректного поиска фраз с пробелами.

Функции не зависят от конфига и не имеют побочных эффектов — легко тестируемы.
"""

from __future__ import annotations

import re
import unicodedata

# ─── Таблица гомоглифов (str.maketrans) ───────────────────────────────────────
# Применяется в deobfuscate() ПОСЛЕ lower(), поэтому только строчные символы.
#
# Покрытые случаи:
#   Визуально похожая латиница → кириллица (частый трюк для обхода фильтров)
#   Цифры, которые читаются как буквы: 0→о, 3→з
#   Нестандартный ë (U+00EB) → ё (U+0451)

_HOMOGLYPH_TABLE = str.maketrans(
    {
        # ── Строчная латиница → кириллица ────────────────────────────────────
        "a": "а",   # U+0061 → U+0430
        "e": "е",   # U+0065 → U+0435
        "o": "о",   # U+006F → U+043E
        "p": "р",   # U+0070 → U+0440
        "c": "с",   # U+0063 → U+0441
        "x": "х",   # U+0078 → U+0445
        "y": "у",   # U+0079 → U+0443
        "k": "к",   # U+006B → U+043A
        "b": "ь",   # U+0062 → U+044C  (спорный, но встречается)
        "m": "м",   # U+006D → U+043C
        "h": "н",   # U+0068 → U+043D  (в некоторых шрифтах похожи)
        # ── Нестандартные варианты ё ─────────────────────────────────────────
        "ë": "ё",   # U+00EB  (диакритика, иногда копируется из Word)
        "ѐ": "е",   # U+0450  (е с грависом, устаревший символ)
        # ── Цифры → буквы ────────────────────────────────────────────────────
        "0": "о",   # ноль → о
        "3": "з",   # тройка → з
    }
)

# Паттерн для zero-width и невидимых управляющих символов
_ZERO_WIDTH = re.compile(
    r"[­​‌‍‎‏⁠⁡⁢⁣﻿]"
)

# Паттерн для символов эмодзи (широкий диапазон Unicode)
_EMOJI = re.compile(
    r"[\U0001F300-\U0001F9FF"    # Misc symbols, emoticons, transport...
    r"\U00002600-\U000027BF"     # Misc symbols, dingbats
    r"\U0000FE00-\U0000FE0F"     # Variation selectors
    r"\U0001FA00-\U0001FA6F"     # Chess, extended symbols
    r"\U0001FA70-\U0001FAFF"     # Symbols and pictographs extended-A
    r"\U00002702-\U000027B0"     # Dingbats
    r"]",
    flags=re.UNICODE,
)

# Символы, которые normalize() заменяет пробелом (всё кроме букв, цифр, пробела)
_NON_WORD = re.compile(r"[^\w\s]", re.UNICODE)

# Множественные пробелы → один
_MULTI_SPACE = re.compile(r"\s+")


# ─── Публичные функции ────────────────────────────────────────────────────────

def normalize(text: str) -> str:
    """Канонический текст для хеширования и сравнения объявлений (раздел 6.4).

    Алгоритм:
      1. Нижний регистр.
      2. Удалить эмодзи (они не несут смысла для сравнения текстов).
      3. Заменить всё кроме букв, цифр, пробела → пробел (пунктуация не нужна).
      4. Схлопнуть whitespace, обрезать края.

    Результат: строка вида "продам велосипед forward 24 хорошее состояние".
    Именно её hashlib.sha256 и difflib.SequenceMatcher сравнивают в duplicates.py.
    """
    text = text.lower()
    text = _EMOJI.sub(" ", text)
    text = _NON_WORD.sub(" ", text)
    text = _MULTI_SPACE.sub(" ", text).strip()
    return text


def deobfuscate(text: str) -> str:
    """Текст для поиска стоп-слов с защитой от типичных трюков (раздел 6.2).

    Алгоритм:
      1. Нижний регистр.
      2. Удалить zero-width и невидимые управляющие символы
         (скрытые разделители между буквами, обходящие слово-границы).
      3. Применить таблицу гомоглифов: лат. буквы → кир., цифры → буквы.
      4. Схлопнуть множественные пробелы (одиночные — сохранить!).

    ВАЖНО: пунктуация намеренно не удаляется — она нужна для:
      а) правильной работы \b-границ в регулярках поиска стоп-слов;
      б) корректного нахождения фраз вида «к продаже» (пробел между словами).
    """
    text = text.lower()
    text = _ZERO_WIDTH.sub("", text)
    text = text.translate(_HOMOGLYPH_TABLE)
    text = _MULTI_SPACE.sub(" ", text).strip()
    return text


def collapse(text: str) -> str:
    """Убрать все не-буквенные символы для поиска «расклеенных» слов.

    Вспомогательная функция для детектора: ловит варианты «п р о д а м»
    и «про-дам», когда \b-поиск по deobfuscate() не срабатывает из-за
    пробелов/дефисов внутри слова.

    Оставляет только кириллицу и латиницу (строчные), цифры удаляет —
    иначе «30000руб» ложно сольётся со стоп-словами.
    """
    return re.sub(r"[^а-яёa-z]", "", text.lower())
